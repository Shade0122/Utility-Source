				TeleportService.CFrame = CFrame.new(1531.30652, 238.231033, -502.034363) -- Red
				wait()
				TeleportService.CFrame = CFrame.new(1316.39172, 228.249802, -594.59729) -- Yellow
				wait()
				TeleportService.CFrame = CFrame.new(1241.65955, 219.724152, -368.719696) -- Blue
				wait()
			end
		end
	else
		HoopFarmButton.Text = "OFF"
		getgenv().HoopFarm = false
	end
end)

